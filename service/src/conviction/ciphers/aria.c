    t[2]  = d[1]^d[4]^d[6]^d[10]^d[11]^d[12]^d[15];
    t[3]  = d[0]^d[5]^d[7]^d[10]^d[11]^d[13]^d[14];
    t[4]  = d[0]^d[2]^d[5]^d[8]^d[11]^d[14]^d[15];
    t[5]  = d[1]^d[3]^d[4]^d[9]^d[10]^d[14]^d[15];
    t[6]  = d[0]^d[2]^d[7]^d[9]^d[10]^d[12]^d[13];
    t[7]  = d[1]^d[3]^d[6]^d[8]^d[11]^d[12]^d[13];
    t[8]  = d[0]^d[1]^d[4]^d[7]^d[10]^d[13]^d[15];
