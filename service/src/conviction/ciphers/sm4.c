static uint32_t sm4_tau(uint32_t a) {
    return ((uint32_t)SM4_SBOX[(a >> 24) & 0xFF] << 24) |
           ((uint32_t)SM4_SBOX[(a >> 16) & 0xFF] << 16) |
           ((uint32_t)SM4_SBOX[(a >> 8) & 0xFF] << 8) |
           ((uint32_t)SM4_SBOX[a & 0xFF]);
}

static uint32_t sm4_l(uint32_t b) {
    return b ^ SAR_ROTL32(b,2) ^ SAR_ROTL32(b,10) ^ SAR_ROTL32(b,18) ^ SAR_ROTL32(b,24);
}

static uint32_t sm4_lp(uint32_t b) {
    return b ^ SAR_ROTL32(b,13) ^ SAR_ROTL32(b,23);
}

void sm4_key_schedule(sm4_ctx_t *ctx, const uint8_t *key) {
    uint32_t K[4];
    K[0] = sar_be32(key)    ^ SM4_FK[0];
    K[1] = sar_be32(key+4)  ^ SM4_FK[1];
    K[2] = sar_be32(key+8)  ^ SM4_FK[2];
    K[3] = sar_be32(key+12) ^ SM4_FK[3];
    for (int i = 0; i < 32; i++) {
        uint32_t tmp = sm4_lp(sm4_tau(K[1] ^ K[2] ^ K[3] ^ SM4_CK[i]));
        ctx->rk[i] = K[0] ^ tmp;
        K[0] = K[1]; K[1] = K[2]; K[2] = K[3]; K[3] = ctx->rk[i];
    }
}

int sm4_encrypt_block(void *vctx, const uint8_t *in, uint8_t *out) {
    sm4_ctx_t *ctx = (sm4_ctx_t *)vctx;
    uint32_t X[4];
    X[0]=sar_be32(in); X[1]=sar_be32(in+4); X[2]=sar_be32(in+8); X[3]=sar_be32(in+12);
    for (int i = 0; i < 32; i++) {
        uint32_t t = sm4_l(sm4_tau(X[1] ^ X[2] ^ X[3] ^ ctx->rk[i]));
        uint32_t x4 = X[0] ^ t;
        X[0]=X[1]; X[1]=X[2]; X[2]=X[3]; X[3]=x4;
    }
    sar_st_be32(out,X[3]); sar_st_be32(out+4,X[2]); sar_st_be32(out+8,X[1]); sar_st_be32(out+12,X[0]);
    return 1;
}

int sm4_decrypt_block(void *vctx, const uint8_t *in, uint8_t *out) {
    sm4_ctx_t *ctx = (sm4_ctx_t *)vctx;
    uint32_t X[4];
    X[0]=sar_be32(in); X[1]=sar_be32(in+4); X[2]=sar_be32(in+8); X[3]=sar_be32(in+12);
    for (int i = 31; i >= 0; i--) {
        uint32_t t = sm4_l(sm4_tau(X[1] ^ X[2] ^ X[3] ^ ctx->rk[i]));
        uint32_t x4 = X[0] ^ t;
        X[0]=X[1]; X[1]=X[2]; X[2]=X[3]; X[3]=x4;
    }
    sar_st_be32(out,X[3]); sar_st_be32(out+4,X[2]); sar_st_be32(out+8,X[1]); sar_st_be32(out+12,X[0]);
    return 1;
}